#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>

void main()
{
    int num[5];
    int i, sum = 0;
    double avg;

    for (i = 0; i < 5; i++) {
        printf("숫자를 입력하세요 : ");
        scanf("%d", &num[i]);

        sum += num[i];
    }

    avg = sum / 5.0;

    printf("합은 %d, 평균은 %lf 입니다.", sum, avg);

 }